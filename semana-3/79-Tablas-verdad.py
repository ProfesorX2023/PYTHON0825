print("Conjunción")
print(f"{True} y {True} : {True and True}")
print(f"{True} y {False} : {True and False}")
print(f"{False} y {True} : {False and True}")
print(f"{False} y {False} : {False and False}")

print("Disyunción")
print(f"{True} o {True} : {True or True}")
print(f"{True} o {False} : {True or False}")
print(f"{False} o {True} : {False or True}")
print(f"{False} o {False} : {False or False}")

print(not(False))
print(not(True))